export function getQuoteForDate(date: Date): Quote {
  const daysSinceEpoch = Math.floor(date.getTime() / 86_400_000);
  const index = ((daysSinceEpoch % quotes.length) + quotes.length) % quotes.length;
  return quotes[index];
}
